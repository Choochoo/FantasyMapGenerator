namespace Language
{
    /// <summary>
    /// Base class representing a named set used in language generation.
    /// Provides a foundation for various phoneme and linguistic element collections.
    /// </summary>
    public class Set
    {
        /// <summary>
        /// The name identifier for this set.
        /// Used to reference and categorize different linguistic element collections.
        /// </summary>
        public string Name;
    }
}
